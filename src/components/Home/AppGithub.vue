  },
  methods: {
    githubAnim() {
      gsap.set(".company, .access", {
        transformOrigin: "50% 50%",
      });

      gsap
        .timeline({
          scrollTrigger: {
            trigger: "#milestone-github",
            toggleActions: this.toggleConfig,
            start: this.startConfig,
          },
          defaults: {
            duration: 1.5,
          },
        })
        .add("github")
        .fromTo(
          ".thought-bubbles g",
          {
            opacity: 0,
            scale: 0,
            rotation: -30,
            transformOrigin: "50% 50%",
          },
          {
            opacity: 1,
            scale: 1,
            stagger: -0.3,
            rotation: 0,
            ease: "back.out(1.7)",
          },
          "github"
        )
        .fromTo(
          ".access",
          {
